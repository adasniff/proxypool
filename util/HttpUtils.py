class HttpUtils(object):

    @staticmethod
    def get_agent():
        return random.choice(USER_AGENTS)

    @staticmethod
    def get_header():
        return {
        'User-Agent': random.choice(USER_AGENTS),
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
        'Accept-Language': 'zh-CN,zh;q=0.8,en;q=0.6',
        'Connection': 'keep-alive',
        'Accept-Encoding': 'gzip, deflate',
    }

    @staticmethod
    def http_get(url, headers=None, proxies=None, cookies=None, timeout=10):
        new_headers = HttpUtils.get_header() if headers is None else HttpUtils.get_header().update(headers)
        try:
            r = requests.get(url, headers=new_headers, proxies=proxies, cookies=cookies, timeout=timeout)
            return r
        except Exception as e:
            return None

    @staticmethod
    def http_get_text(url, headers=None, proxies=None, cookies=None, timeout=10):
        r = HttpUtils.http_get(url, headers, proxies, cookies, timeout)
        if r is None or not r.ok:
            return None
        return r.text

    @staticmethod
    def http_get_json(url, headers=None, proxies=None, cookies=None, timeout=10):
        r = HttpUtils.http_get(url, headers, proxies, cookies, timeout)
        try:
            return r.json()
        except Exception:
            return None
